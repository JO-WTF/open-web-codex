export function extractRenameText(text: string) {
  if (!text) {
    return "";
  }
  const withoutImages = text.replace(/\[image(?: x\d+)?\]/gi, " ");
  const withoutSkills = withoutImages.replace(/(^|\s)\$[A-Za-z0-9_-]+(?=\s|$)/g, " ");
  return withoutSkills.replace(/\s+/g, " ").trim();
}

function getAssistantTextForRename(
  items: ConversationItem[],
  itemId?: string,
): string {
  if (itemId) {
    const match = items.find(
      (item) =>
        item.kind === "message" &&
        item.role === "assistant" &&
        item.id === itemId,
    );
    if (match && match.kind === "message") {
      return match.text;
    }
  }
  for (let index = items.length - 1; index >= 0; index -= 1) {
    const item = items[index];
    if (item.kind === "message" && item.role === "assistant") {
      return item.text;
    }
  }
  return "";
}

export function maybeRenameThreadFromAgent({
  workspaceId,
  threadId,
  items,
  itemId,
  hasCustomName,
  threadsByWorkspace,
}: {
  workspaceId: string;
  threadId: string;
  items: ConversationItem[];
  itemId?: string;
  hasCustomName: boolean;
  threadsByWorkspace: ThreadState["threadsByWorkspace"];
}) {
  const threads = threadsByWorkspace[workspaceId] ?? [];
  if (!threads.length) {
    return threadsByWorkspace;
  }
  const hasUserMessage = items.some(
    (item) => item.kind === "message" && item.role === "user",
  );
  if (hasUserMessage) {
    return threadsByWorkspace;
  }
  if (hasCustomName) {
    return threadsByWorkspace;
  }
  const nextName = clampThreadName(getAssistantTextForRename(items, itemId));
  if (!nextName) {
    return threadsByWorkspace;
  }
  let didChange = false;
  const nextThreads = threads.map((thread) => {
    if (
      thread.id !== threadId ||
      thread.name === nextName ||
      !looksAutoGeneratedThreadName(thread.name)
    ) {
      return thread;
    }
    didChange = true;
    return { ...thread, name: nextName };
  });
  return didChange
    ? { ...threadsByWorkspace, [workspaceId]: nextThreads }
    : threadsByWorkspace;
}

const LEADING_PROVIDER_SENTINELS = [
  "<｜begin▁of▁sentence｜>",
  "<|begin_of_sentence|>",
  "<｜begin_of_sentence｜>",
];

export function stripLeadingProviderSentinel(text: string) {
  let next = text;
  let didStrip = true;
  while (didStrip) {
    didStrip = false;
    const leadingWhitespace = next.match(/^\s*/)?.[0] ?? "";
    const body = next.slice(leadingWhitespace.length);
    for (const sentinel of LEADING_PROVIDER_SENTINELS) {
      if (body.startsWith(sentinel)) {
        next = `${leadingWhitespace}${body.slice(sentinel.length)}`;
        didStrip = true;
        break;
      }
    }
  }
  return next;
}

export function mergeStreamingText(existing: string, delta: string) {
  const cleanDelta = stripLeadingProviderSentinel(delta);
  if (!cleanDelta) {
    return existing;
  }
  if (!existing) {
    return cleanDelta;
  }
  if (cleanDelta === existing) {
    return existing;
  }
  if (cleanDelta.startsWith(existing)) {
    return cleanDelta;
  }
  if (existing.startsWith(cleanDelta)) {
    return existing;
  }
  const maxOverlap = Math.min(existing.length, cleanDelta.length);
  for (let length = maxOverlap; length > 0; length -= 1) {
    if (existing.endsWith(cleanDelta.slice(0, length))) {
      return `${existing}${cleanDelta.slice(length)}`;
    }
  }
  return `${existing}${cleanDelta}`;
}

export function addSummaryBoundary(existing: string) {
  if (!existing) {
    return existing;
  }
  if (existing.endsWith("\n\n")) {
    return existing;
  }
  if (existing.endsWith("\n")) {
    return `${existing}\n`;
  }
  return `${existing}\n\n`;
}

export function dropLatestLocalReviewStart(list: ConversationItem[]) {
  for (let index = list.length - 1; index >= 0; index -= 1) {
    const item = list[index];
    if (
      item.kind === "review" &&
      item.state === "started" &&
      item.id.startsWith("review-start-")
    ) {
      return [...list.slice(0, index), ...list.slice(index + 1)];
    }
  }
  return list;
}

export function findMatchingReview(
  list: ConversationItem[],
  target: Extract<ConversationItem, { kind: "review" }>,
) {
  const normalizedText = target.text.trim();
  return list.find(
    (item) =>
      item.kind === "review" &&
      item.state === target.state &&
      item.text.trim() === normalizedText,
  );
}

export function ensureUniqueReviewId(list: ConversationItem[], item: ConversationItem) {
  if (item.kind !== "review") {
    return item;
  }
  if (!list.some((entry) => entry.id === item.id)) {
    return item;
  }
  const existingIds = new Set(list.map((entry) => entry.id));
  let suffix = 1;
  let candidate = `${item.id}-${suffix}`;
  while (existingIds.has(candidate)) {
    suffix += 1;
    candidate = `${item.id}-${suffix}`;
  }
  return { ...item, id: candidate };
}

export function isDuplicateReviewById(
  list: ConversationItem[],
  target: Extract<ConversationItem, { kind: "review" }>,
) {
  const normalizedText = target.text.trim();
  return list.some(
    (item) =>
      item.kind === "review" &&
      item.id === target.id &&
      item.state === target.state &&
      item.text.trim() === normalizedText,
  );
}

export function prefersUpdatedSort(state: ThreadState, workspaceId: string) {
  return (state.threadSortKeyByWorkspace[workspaceId] ?? "updated_at") === "updated_at";
}
