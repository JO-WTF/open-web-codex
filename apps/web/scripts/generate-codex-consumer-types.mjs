import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const GENERATED_DIR = resolve(ROOT, "contracts/codex/generated");
const RUST_GENERATED = resolve(ROOT, "crates/codex-contracts/src/generated.rs");

const MANIFEST_SCHEMA_PATH = resolve(ROOT, "contracts/codex/capability-manifest.schema.json");
const FEATURE_POLICY_PATH = resolve(ROOT, "contracts/codex/policy/feature-policy.v1.json");

const GENERATED_HEADER = `// @generated by scripts/generate-codex-consumer-types.mjs — do not edit by hand\n`;

function readJson(path) {
  return JSON.parse(readFileSync(path, "utf8"));
}

function capabilityStatuses(schema) {
  return schema.$defs.capability.properties.status.enum;
}

function renderTypeScript(manifestSchema, featurePolicy) {
  const statuses = capabilityStatuses(manifestSchema)
    .map((value) => `"${value}"`)
    .join(" | ");

  const featureIds = featurePolicy.features.map((feature) => `"${feature.id}"`).join(" | ");

  return `${GENERATED_HEADER}
export type CapabilityStatus = ${statuses};

export interface StructuredReason {
  code: string;
  message: string;
  remediation?: string;
  minimumServerVersion?: string;
}

export interface MethodSet {
  clientRequests?: string[];
  serverRequests?: string[];
  notifications?: string[];
}

export interface CapabilityDeclaration {
  id: string;
  version: string;
  status: CapabilityStatus;
  experimental?: boolean;
  methods: MethodSet;
  limits: Record<string, string | number | boolean | null>;
  reason?: StructuredReason;
  metadata?: Record<string, unknown>;
}

export interface ServerIdentity {
  protocolVersion: string;
  buildVersion: string;
  commit: string;
  target: string;
}

export interface CompatibilityRange {
  minimumClientProtocol: string;
  maximumClientProtocol: string;
}

export interface CapabilityManifest {
  schemaVersion: string;
  generatedAt: string;
  server: ServerIdentity;
  compatibility: CompatibilityRange;
  capabilities: CapabilityDeclaration[];
}

export type FeaturePolicyId = ${featureIds};

export interface FeaturePolicyEntry {
  id: FeaturePolicyId;
  capabilityId: string;
  minimumCapabilityVersion: string;
  allowedStatuses: CapabilityStatus[];
  requiresExplicitEnable?: boolean;
}

export interface FeaturePolicy {
  schemaVersion: string;
  description?: string;
  features: FeaturePolicyEntry[];
}
`;
}

function renderRust(manifestSchema, featurePolicy) {
  const statuses = capabilityStatuses(manifestSchema);
  const statusVariants = statuses
    .map((value) => {
      const variant = value
        .split("_")
        .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
        .join("");
      return `    #[serde(rename = "${value}")]\n    ${variant},`;
    })
    .join("\n");

  const featureIds = featurePolicy.features
    .map((feature) => `    #[serde(rename = "${feature.id}")]\n    ${toPascalCase(feature.id)},`)
    .join("\n");

  return `// @generated by scripts/generate-codex-consumer-types.mjs — do not edit by hand

use serde::{Deserialize, Serialize};
use std::collections::HashMap;

#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
#[serde(rename_all = "camelCase")]
pub struct CapabilityManifest {
    pub schema_version: String,
    pub generated_at: String,
    pub server: ServerIdentity,
    pub compatibility: CompatibilityRange,
    pub capabilities: Vec<CapabilityDeclaration>,
}

#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
#[serde(rename_all = "camelCase")]
pub struct ServerIdentity {
    pub protocol_version: String,
    pub build_version: String,
    pub commit: String,
    pub target: String,
}

#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
#[serde(rename_all = "camelCase")]
pub struct CompatibilityRange {
    pub minimum_client_protocol: String,
    pub maximum_client_protocol: String,
}

#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
#[serde(rename_all = "camelCase")]
pub struct CapabilityDeclaration {
    pub id: String,
    pub version: String,
    pub status: CapabilityStatus,
    #[serde(default, skip_serializing_if = "std::ops::Not::not")]
    pub experimental: bool,
    pub methods: MethodSet,
    pub limits: HashMap<String, serde_json::Value>,
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub reason: Option<StructuredReason>,
}

#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
#[serde(rename_all = "camelCase")]
pub struct MethodSet {
    #[serde(default, skip_serializing_if = "Vec::is_empty")]
    pub client_requests: Vec<String>,
    #[serde(default, skip_serializing_if = "Vec::is_empty")]
    pub server_requests: Vec<String>,
    #[serde(default, skip_serializing_if = "Vec::is_empty")]
    pub notifications: Vec<String>,
}

#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
#[serde(rename_all = "camelCase")]
pub struct StructuredReason {
    pub code: String,
    pub message: String,
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub remediation: Option<String>,
}

#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
pub enum CapabilityStatus {
${statusVariants}
}

#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
#[serde(rename_all = "camelCase")]
pub struct FeaturePolicy {
    pub schema_version: String,
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub description: Option<String>,
    pub features: Vec<FeaturePolicyEntry>,
}

#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
#[serde(rename_all = "camelCase")]
pub struct FeaturePolicyEntry {
    pub id: String,
    pub capability_id: String,
    pub minimum_capability_version: String,
    pub allowed_statuses: Vec<CapabilityStatus>,
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub requires_explicit_enable: Option<bool>,
}

pub const FEATURE_POLICY_FEATURE_IDS: &[&str] = &[
${featurePolicy.features.map((feature) => `    "${feature.id}",`).join("\n")}
];
`;
}

function toPascalCase(value) {
  return value
    .split(/[._]/)
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join("");
}

export function generateConsumerTypes({
  manifestSchemaPath = MANIFEST_SCHEMA_PATH,
  featurePolicyPath = FEATURE_POLICY_PATH,
  outputDir = GENERATED_DIR,
  rustOutputPath = RUST_GENERATED,
} = {}) {
  const manifestSchema = readJson(manifestSchemaPath);
  const featurePolicy = readJson(featurePolicyPath);

  mkdirSync(outputDir, { recursive: true });
  mkdirSync(dirname(rustOutputPath), { recursive: true });

  const tsPath = resolve(outputDir, "capability-manifest.types.ts");
  const featureTsPath = resolve(outputDir, "feature-policy.types.ts");
  const tsContent = renderTypeScript(manifestSchema, featurePolicy);
  const featureTsContent = `${GENERATED_HEADER}
import type { CapabilityStatus } from "./capability-manifest.types";

export type FeaturePolicyId = ${featurePolicy.features.map((feature) => `"${feature.id}"`).join(" | ")};

export interface FeaturePolicyEntry {
  id: FeaturePolicyId;
  capabilityId: string;
  minimumCapabilityVersion: string;
  allowedStatuses: CapabilityStatus[];
  requiresExplicitEnable?: boolean;
}

export interface FeaturePolicy {
  schemaVersion: string;
  description?: string;
  features: FeaturePolicyEntry[];
}
`;

  writeFileSync(tsPath, tsContent);
  writeFileSync(featureTsPath, featureTsContent);
  writeFileSync(rustOutputPath, renderRust(manifestSchema, featurePolicy));

  return { tsPath, featureTsPath, rustOutputPath };
}

function main() {
  const result = generateConsumerTypes();
  process.stdout.write(`${JSON.stringify(result)}\n`);
}

if (process.argv[1] && import.meta.url === pathToFileURL(resolve(process.argv[1])).href) {
  main();
}
